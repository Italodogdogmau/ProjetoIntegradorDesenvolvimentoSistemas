    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        if(TblEstudante.getSelectedRow()!= -1){

            Estudante e = new Estudante();
            EstudanteDAO dao = new EstudanteDAO();
 
            e.setNome(txtNome.getText());
            e.setCpf(Long.parseLong(txtCpf.getText()));
            e.setCarga(Integer.parseInt(txtCargaC.getText()));
            e.setEmail(txtEmail.getText());
            e.setSemestre(Integer.parseInt(txtSeme.getText()));
            e.setTelefone(Integer.parseInt(txtTel.getText()));
            e.setMatricula(Integer.parseInt(txtMat.getText()));
            
            try {
                dao.update(e);
                
            } catch (SQLException ex) {
                Logger.getLogger(CadastroEstudante.class.getName()).log(Level.SEVERE, null, ex);
            }
            readTable();
            }else{
            JOptionPane.showMessageDialog(null, "Selecione um Estudante para Alterar.");
        }{
         }
        readTable();
    }                                              

