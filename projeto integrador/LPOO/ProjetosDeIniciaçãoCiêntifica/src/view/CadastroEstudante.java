    public void readTable(){
     DefaultTableModel modelo = (DefaultTableModel)TblEstudante.getModel();
     modelo.setNumRows(0);
     EstudanteDAO edao = new EstudanteDAO();
     
     
     for(Estudante  e: edao.read())
        {
         modelo.addRow(new Object[]{
             e.getIdestudante(),
             e.getIdcurso(),
             e.getNome(),
             e.getCpf(),
             e.getNome_curso(),
             e.getCarga(),
             e.getEmail(),
             e.getSemestre(),
             e.getTelefone(),
             e.getMatricula(),
         });
     }
    }

